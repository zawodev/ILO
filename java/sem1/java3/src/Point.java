public class Point {
    private double x;
    private double y;
    public Point(double x, double y){
        this.x = x;
        this.y = y;
    }
    public void SetX(double val){ x = val;}
    public void SetY(double val){ y = val;}
    public double GetX(){ return x;}
    public double GetY(){ return y;}
}
